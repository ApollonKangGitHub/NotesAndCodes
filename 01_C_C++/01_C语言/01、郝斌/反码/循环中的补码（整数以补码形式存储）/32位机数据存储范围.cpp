# include <stdio.h>
int main (void)
{
	int i;
	for(i=1;i>0;i++)
	{
		;
	}

	printf("%d\n",i);

	return 0;
}

/*
输出结果-2147483648并非是乱码，
而是32位机（VC++6.0编译器是32位）能存储的的最小负数。

32位机存储范围[-2147483648,2147483647)

i++=2147483647（0111 1111 1111 1111 1111  1111 1111 1111(补码)）时i>0成立，
再i++=-2147483648（1000 0000 0000 0000 0000 0000 0000 0000(补码)）时i<0退出循环
所以显示-2147483648
*/